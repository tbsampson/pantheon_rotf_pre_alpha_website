<p><span style="font-size: 16px;">Visionary Realms, Inc. was founded by Brad McQuaid and an elite group of experienced MMO developers. We've worked on EverQuest (original game and just about every expansion), EverQuest 2 (original game and just about every expansion), Vanguard: Saga of Heroes, EverQuest Next, Free Realms, Star Wars: Galaxies, Star Trek: Hidden Evil, Myst 3: Exile, and more.</span></p>
<p><span style="font-size: 16px;">Brad McQuaid has been involved in game development for over 20 years. He was the original producer and co-designer of EverQuest, the iconic game that helped usher in the entire MMO genre. Brad managed the development team from the project's inception until its launch. He then assumed the role of Executive Producer for the first few EverQuest expansions, still celebrated by many as some of the best content in the game's 14-year history.</span></p>
<p><span style="font-size: 16px;">Brad was also a founder of Verant Interactive and its Vice President. After Verant's acquisition by Sony Online Entertainment, Brad served at SOE as Vice President of Premium Games responsible for the hit games EverQuest, EverQuest 2, EverQuest Online Adventures, PlanetSide, and Star Wars: Galaxies. In addition, Brad was the company's Chief Creative Officer.</span></p>
<p><span style="font-size: 16px;">After leaving SOE in October 2001, Brad took a short break and then co-founded Sigil Games Online where he took on the role of CEO and co-executive-producer. The company began development of the massively multiplayer Vanguard: Saga of Heroes. Over the next five years Sigil grew to over 100 employees and in January of 2007, Sigil and SOE launched Vanguard: Saga of Heroes. Later that year Sigil sold both the company and Vanguard to Sony Online Entertainment.</span></p>
<p><span style="font-size: 16px;">After the sale, Brad decided to take an extended break from games development to pursue his hobbies and to spend time with friends and family.</span></p>
<p><span style="font-size: 16px;">Unable to stay away from his passion for game development for too long, Brad is back with an all-star team with whom he founded Visionary Realms, Inc. in October, 2013. The team, with nearly a collective century of game development experience, immediately started work on Pantheon: Rise of the Fallen.</span></p>
